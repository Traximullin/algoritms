fn get_grade(s1: u16, s2: u16, s3: u16) -> char {
    let sum = (s1 + s2 + s3) / 3;
    println!("{}", sum);
    match sum {
        90..=100 => 'A',
        80..=90 => 'B',
        70..=80 => 'C',
        60..=70 => 'D',
        _ => 'F'
    }
}
